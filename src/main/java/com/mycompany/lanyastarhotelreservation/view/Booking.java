    private void cmbTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbTypeActionPerformed
        // TODO add your handling code here:   \
        String selected = (String) cmbType.getSelectedItem();

        cmbDestination.removeAllItems(); // CLEAR DESTINATION COMBO

        if ("Local".equals(selected)) {
            for (String item : localDestinations) {
                cmbDestination.addItem(item);
            }
        } 
        else if ("International".equals(selected)) {
            for (String item : internationalDestinations) {
                cmbDestination.addItem(item);
            }
        } 
        else {
            cmbDestination.addItem("--Select Destination--");
        }
        
    }//GEN-LAST:event_cmbTypeActionPerformed

